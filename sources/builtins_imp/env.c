#include "minishell.h"

void	envp(char **env)
{
	if (env != NULL)
	{
		while (*env != NULL)
		{
			if ((ft_strchr(*env, '=')) != NULL)
			{
				write(1, *env, ft_strlen(*env));
				write(1, "\n", 1);
			}
			env++;
		}
	}
}
